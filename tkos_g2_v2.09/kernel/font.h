/*
 * Karakter lookup:
 *   font8x8[c - 0x20][satir]
 * c >= 0x20 && c <= 0x7F olmali
 * Aralik disi karakterler icin space (0x20) kullanilir
 */
static inline const uint8_t *font_get(char c) {
    uint8_t uc = (uint8_t)c;
    if (uc < 0x20 || uc > 0x7F) uc = 0x20;
    return font8x8[uc - 0x20];
}
